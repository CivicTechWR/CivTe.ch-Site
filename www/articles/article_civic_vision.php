		<p>
			<date>June 3, 2017</date>
		</p>
				 
		<p>My name is Jonathan Tonge and I am currently looking to start a CivTech Team.</p>

		<p>Kristina Taylor also shares this vision. Read more about what she is thinking <a href="https://medium.com/code-for-canada/code-for-canada-civic-tech-c09fb15a0140">here</a>.</p>
		<p>Here are some goals:</p>
		<ul>
			<li>Work on projects that have the potential to positively impact people</li>
			<li>Work on projects that are resume builders and have the potential to impress employers and organizations</li>
			<li>Data-driven and use industry best practices in everything we do</li>
			<li>Promote and support our members and recognize high-performers</li>
			<li>Be a tool for the government to discover and help foster talent in our area</li>
			<li>Promote our communities as leaders in Civic Tech</li>
			<li>Create synergies with other groups like CodeFor.ca, Ontario Digital Government and Civic Innovation Lab</li>
		</ul>
		</p>
		<h3>Civic Tech Categories</h3>
		<ul>
			<li>Open government:
				<ul>
				<li>Data access and transparency</li>
				<li>Voting</li>
				<li>Visualization and mapping</li>
				<li>Data utility</li>
				<li>Resident feedback</li>
				<li>Public decision making</li>
				</ul>
			</li>
			<li>Community action:
				<ul>
				<li>Peer-to-peer local sharing</li>
				<li>Civic crowd-funding</li>
				<li>Neighborhood forums</li>
				<li>Information crowdsourcing</li>
				<li>Community organizing</li>
				</ul>
			</li>
		</ul>

		<p>That's how Civic Tech is defined by some organizations. I'm sure we can as a community take it one step further in terms of categorization. The Kitchener and Waterloo tech community has a reputation for disrupting norms and redefining the future. So let's step outside the box.</p>

		<p>Let's take a look at what technology is uniquely available in our community, integrate and promote it. This is an opportunity for everyone including business, government and most important people to benefit.</p>

		<p>Kristina outlined some of the resources that would be required to make this mission a success, including space to meet. It would be nice if we could also leverage a few sponsors as well. It is a great low-cost way to support innovation in your community while being provided with an excellent opportunity to market yourself to the CivTech community and government.</p>



		<h3>Skill Sets</h3>

		<p>Here are some skill sets we should employ:</p>
		<ul>
			<li>Compliance - AODA, WCAG..</li>
			<li>Product Management</li>
			<li>Web Development</li>
			<li>UI</li>
			<li>UX</li>
			<li>Project Management</li>
			<li>Big Data</li>
			<li>Engineering</li>
			<li>Process Design</li>
			<li>Business Planning</li>
			<li>Government / Public Sector Counsel</li>
			<li>Marketing & Proposals</li>
			<li>Human Resources / Organization</li>
			<li>Creative Media - Graphics / Photo / Video / Digital</li>
			<li>Journalism / Content / Editors / Social Media</li>
			<li>Finance, Budget, Admin</li>
		</ul>
		<p>In essence, it's more than what we create. I think to be leaders in CivTech, you need to show an effective scalable organizational model that we can test, improve and share with other organizations including startups. On the same note some of us proudly wear more then one of these hats and will be looking to try new ones on and learn from each other, so an inclusive approach that keeps our interest and builds our professional development is a must.</p>
		<h3>Board of Directors</h3>
		<p>Not sure if this is necessary, but senior leadership is incredibly valuable. It would be nice to have some experienced and proven talent from both the private-sector and government offer high-level advice to help shape us going forward.</p>
	
		
		<h3>Robotics</h3>
		<p>While many CivTech projects are focused exclusively on digital media, we should also consider projects and teams that are capable of deploying tangible products, such as robots leveraging Raspberry Pi. Citizen robots to protect us from all the enemy drones?</p>


